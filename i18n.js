function initI18n() {
  const lang = getCurrentLang();
  setLang(lang);

  // Bind language select
  const langSelect = document.getElementById('lang-select');
  if (langSelect) {
    langSelect.addEventListener('change', function() {
      setLang(this.value);
    });
  }
}
